CREATE TABLE PROFESSORS (
	ID_PROFESSOR NUMBER GENERATED BY DEFAULT AS IDENTITY,
	FIRST_NAME VARCHAR2(100) NOT NULL,
	LAST_NAME VARCHAR2(100) NOT NULL,
	ADDRESS VARCHAR2(100) NOT NULL,
	PHONE_NUMBER VARCHAR2(100) NOT NULL UNIQUE,
	ESPECIALITY VARCHAR2(100) NOT NULL,
	CONSTRAINT PK_PROFESSOR PRIMARY KEY (ID_PROFESSOR)
);
CREATE TABLE BACHILLERS (
	ID_BACHILLER NUMBER GENERATED BY DEFAULT AS IDENTITY,
	BACHILLER VARCHAR2(100) NOT NULL UNIQUE,
	SECTION NUMBER NOT NULL,
	CONSTRAINT PK_BACHILLER PRIMARY KEY (ID_BACHILLER)
);
CREATE TABLE SUBJECTS (
	ID_SUBJECT NUMBER GENERATED BY DEFAULT AS IDENTITY,
	SUBJECT VARCHAR2(100) NOT NULL UNIQUE,
	ID_BACHILLER NUMBER NOT NULL,
	ID_PROFESSOR NUMBER NOT NULL,
	CONSTRAINT PK_SUBJECT PRIMARY KEY (ID_SUBJECT),
	CONSTRAINT FK_SUBJECT_BACHILLER FOREIGN KEY (ID_BACHILLER) REFERENCES BACHILLERS(ID_BACHILLER),
	CONSTRAINT FK_SUBJECT_PROFESSOR FOREIGN KEY (ID_PROFESSOR) REFERENCES PROFESSORS(ID_PROFESSOR)
);
